package org.abstractmeta.reflectify.example;

/**
 * Represents Department
 * <p>
 * </p>
 *
 * @author Adrian Witas
 */
public class Department {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
